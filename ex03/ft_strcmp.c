int		ft_strcmp(char *s1, char *s2)
{
	int		i;
	
	i = 0;
	while (s1 != NULL && s2 != NULL)
	{
		if (s1[i] != s2[i])
		{
		return (s1[i] - s2[i]);
		}
		i++;

	}
	if (s1[i] != NULL)
	{
		return (s1[i]);
	}
	if (s2[i] != NULL)
	{
		return (s2[i]);
	}
	return (0);
}

